                            <h4 class="lista__texto">Título y Subtítulos</h4>

                            <ul>
                                <li>Subtítulos: Times New Roman, 12; alineación a la izquierda y en negrita.</li>
                                <li>Elimine toda la edición, formateo y numeración automática. Organice los títulos y
                                    subtítulos utilizando una jerarquización clara; no utilice más de tres niveles de
                                    encabezamiento. Haga referencias únicamente a las secciones, no a páginas
                                    concretas.</li>
                                <li>Los títulos y subtítulos del artículo siemp deberán escribirse en letras redondas
                                    (nunca en cursivas) y sin puntos. Solo su primera letra irá en mayúsculas –a menos
                                    que incluya un nombre propio, en cuyo caso su primera letra también irá en
