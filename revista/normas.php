                            de los resultados de este proceso, el comité editorial decidirá sobre la publicación
                            de los artículos e informará a los autores al respecto. En caso de ser aceptado el
                            artículo, el plazo máximo para su publicación es de un año (contado desde la
                            recepción de la colaboración). Cabe mencionar que otras secciones (como
                            Entrevistas, Tesis y Reseña) están a cargo del Área de investigaciones de Lava
                            Perú.
