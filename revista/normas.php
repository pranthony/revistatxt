                        <li>
                            <h4 class="lista__texto">Citas en el texto</h4>

                            <ul>
                                <li>pferentemente se solicita que el estilo de cita y referencia está basado en el
                                    Sistema APA (Asociación Americana de Psicología)</li>
                                <li>Las notas al pie de página se usarán solamente para realizar comentarios
                                    complementarios, mas no para citas bibliográficas.</li>
                                <li>No use “ibidem” ni “ibid.”.</li>
                                <li>Las citas textuales que van al interior del texto deben estar entre comillas latinas
                                    o
                                    españolas (« ») y en redondas (letra normal).</li>
                                <li>Si el fragmento citado incluye, a su vez, una cita en su interior o una frase entre
                                    comillas, se recurrirá a las inglesas (‘‘ ‘‘). El punto siemp irá inmediatamente
                                    después de las comillas. Ejemplo:
                                    <p>En relación con este tema, afirma un destacado lingüista: «La idea según la cual
                                        “el
                                        igualitarismo es doctrina aplicable a los hombres, pero no en absoluto a los
                                        idiomas” (Salvador 1992: 97) no tiene base lingüística alguna» (Moreno Cabrera
                                        2000: 233).</p>
                                </li>
                                <li>En el caso de que el fragmento citado contenga, a su vez, una cita de otro autor
                                    y este último ha empleado el sistema clásico de citado, se recomienda convertir la
                                    cita en sistema parentético y así consignarla en el fragmento que se desee citar.
                                    Ejemplo:
                                    <ol style="list-style: lower-alpha">
                                        <li>Fragmento que se desea citar y que contiene una cita con una referencia en
                                            sistema clásico de citado. <br>
                                            El proceso […] conocido como “deslocalización de servicios” constituye un
                                            mercado potencial de $330 mil millones que, según los analistas, solo
                                            estaría
                                            aprovechado en un 10% actualmente y que podría llegar a significar más de
                                            110
                                            billones de dólares en el año 2010 (Ribeiro, 2005).
                                        </li>
                                        <br>
                                        <li>Fragmento anterior citado y con la referencia de la cita interna convertida
                                            en el
                                            sistema parentético. <br>
                                            Como señala Bossio (2008: 2): El proceso […] conocido como “deslocalización
                                            de servicios” constituye un mercado potencial de $330 mil millones que,
                                            según los
                                            analistas (Ribeiro 2005), solo estaría aprovechado en un 10% actualmente y
                                            que
                                            podría llegar a significar más de 110 billones de dólares en el año 2010.
                                        </li>
                                        <br>
                                    </ol>
                                </li>
                                <li>En última instancia, cuando no hay modo de acceder directamente a una
                                    determinada fuente que es importante para la investigación, también es posible
                                    (aunque no es lo ideal) citar un fragmento citado, a su vez, por otro autor. En
                                    ese caso, la referencia parentética debe incluir, en primer lugar, el apellido del
                                    autor del fragmento, seguido de una coma; a continuación, la expsión “citado
                                    por”; y, finalmente, los datos de la fuente realmente consultada. Ejemplo:
                                    <br>
                                    <p>Las investigaciones sociolingüísticas de los últimos años han descartado de plano
                                        que las modalidades dialectales no estándares sean lógica o comunicativamente
                                        inferiores a la estándar. Así lo indica un importante investigador americano:
                                        “Una
                                        observación detallada de las pruebas empíricas niega la psunción de que el Black
                                        English sea menos lógico que el inglés estándar o de que no sea competente para
                                        las abstracciones” (Baugh, citado por Moreno Cabrera 2000: 58).</p>
                                    <br>
                                </li>
                                <li>Cuando se omite información en una cita textual, se deben emplear corchetes y
                                    tres puntos suspensivos en reemplazo del fragmento omitido ([…]), cuya
                                    ubicación corresponde con la de los corchetes.</li>
                                <li>Cabe señalar que de tratarse de una cita textual mayor a cuatro líneas, el formato
                                    cambia. Se deben componer a espacio simple, sin comillas y en redondas. Deben
                                    ir con un margen adicional de doce a quince espacios a partir del comienzo de las
                                    líneas normales. La referencia bibliográfica se coloca entre paréntesis al final del
                                    párrafo. Por ejemplo:
                                    <br>
                                    <p>Quiero que [los señores] a quienes acabo de hablar sepan, y todos los
                                        honorables congresistas también, que la Constitución de 1861, que fue una
                                        muy buena, me la llevo en este bolsillo (izquierdo), y que la de 1868, que es
                                        incluso mejor de acuerdo a estos [señores], me la llevo en este otro bolsillo,
                                        y que nadie sino yo gobierna Bolivia (Loveman 1993: 252).</p>
                                    <br>
                                </li>
                                <li>La información bibliográfica entre paréntesis debe incluir el apellido del autor
                                    citado, el año de publicación de la obra citada y el número de la(s) página(s) de
                                    la(s) que se ha tomado la información en caso corresponda. Los paréntesis deben
                                    colocarse inmediatamente después de la cita –pcedidos por un espacio– y antes
                                    del punto que la cierra. Ejemplo: «Los obreros siguieron la misma tendencia»
                                    (Verdera 2007: 228).</li>
                                <li>En caso de que se incluyan varias obras de un mismo autor publicadas en un
                                    mismo año, será necesario incluir letras minúsculas en orden alfabético que sirvan
                                    para diferenciar las obras entre sí. Ejemplo: (Agueda 2011a: 66).</li>
                                <li>Si el texto pertenece a un medio periodístico o es un trabajo colectivo de una
                                    organización en el que no se consigna el nombre de un autor, tanto la referencia
                                    bibliográfica como la cita deben reflejar eso. Nótese que, en el caso de
                                    publicaciones periódicas, el nombre de la publicación va en cursivas. Asimismo, si
                                    se trata de una organización de nombre muy extenso, se puede emplear su
                                    acrónimo o su sigla. Ejemplos: (Noticias SER 2011), (Midis 2011).</li>
                                <li>Cuando la obra citada tenga dos autores, deben figurar sus apellidos separados por
                                    un «y», como en el ejemplo a continuación: (Dargent y Muñoz 2010). Si hay más
                                    de dos autores, solo se citará al primero, seguido por la abreviatura latina «et
                                    al.»,
                                    que significa «y otros». Nótese que solo lleva un punto, al final, y que por ser un
                                    préstamo del latín debe escribirse en cursivas. Ejemplo: (Morlino et al. 2009).
                                    Únicamente emplearemos «et al.» en las referencias parentéticas: en la bibliografía
                                    es necesario citar los nombres completos (nombre y apellido) de todos los autores
                                    del texto citado.</li>
                            </ul>
                        </li>
