                            <h4>Texto</h4>

                            <ul class="lista__texto">
                                <li>Normal (Times New Román, tamaño 12, interlineado 1,5), justificado.</li>
                                <li>Tamaño de la hoja: A4. Configuración de página estándar Word (márgenes
                                    superior/inferior 2.5cm e izquierdo/derecho 3cm).</li>
                                <li> Notas al pie: Times New Roman, tamaño 10.</li>
                                <li>Paginación: el texto deberá estar numerado en forma correlativa, en la parte
                                    inferior derecha de la página.</li>
                                <li>Se utilizarán las negritas para enfatizar y resaltar términos en el interior del
                                    texto.
                                    Su empleo no es obligatorio, sino más bien depende del estilo personal del autor.
                                </li>
                                <li>Se utilizarán cursivas para términos en idiomas distintos del español.</li>
                                <li>No utilice separación por sílabas.</li>
                                <li>Incluya con moderación notas a pie de página, debe hacerse al final de la oración y
                                    después del signo de puntuación.</li>
