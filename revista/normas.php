                    <h3 class="lista__texto">Normas para el envío de artículos corregidos</h3>
                    <br>
                    <ol>
                        <li>
                            Los artículos deberán tener una extensión mínima de 6 páginas y máxima de 21
                            páginas (nueve mil palabras aproximadamente), incluyendo gráficos, cuadros y
                            notas a pie de página.
                        </li>
                        <li>Los artículos deben ir acompañados de una sumilla (en español y en inglés) que no
                            supere las cien palabras. En la sumilla se especificarán los objetivos principales, el
                            argumento o hipótesis y los resultados de la investigación. Además, se debe
                            indicar cuatro a seis palabras clave en español e inglés.
                        </li>
                        <li>
                            Se deberá señalar claramente el nombre del autor o autora y el de la institución a la
                            que pertenece. En caso de tratarse de varios autores, el autor principal se
                            encargará de conseguir los permisos correspondientes de los otros autores.
                            Asimismo se deberá indicar con un llamado a pie de página si el texto es fruto de
                            un proyecto de investigación financiado por alguna institución.
                        </li>
